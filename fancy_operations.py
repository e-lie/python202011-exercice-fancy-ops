
def fancy_add(n: int, m: int) -> int:
    assert isinstance(n, int)
    assert isinstance(m, int)
    return n + m

def fancy_product(n: int, m: int) -> int:
    assert isinstance(n, int)
    assert isinstance(m, int)
    return n * m

def fancy_substract(n: int, m: int) -> int:
    assert isinstance(n, int)
    assert isinstance(m, int)
    return n - m